package com.ey.in.tds.dto;

public class ExemptionErrorDTO {

	private String deductorType;
	private String shareHolderCatagory;
	private String residentialStatus;
	private String section;
	private String reason;
	
	public String getDeductorType() {
		return deductorType;
	}
	public void setDeductorType(String deductorType) {
		this.deductorType = deductorType;
	}
	public String getShareHolderCatagory() {
		return shareHolderCatagory;
	}
	public void setShareHolderCatagory(String shareHolderCatagory) {
		this.shareHolderCatagory = shareHolderCatagory;
	}
	public String getResidentialStatus() {
		return residentialStatus;
	}
	public void setResidentialStatus(String residentialStatus) {
		this.residentialStatus = residentialStatus;
	}
	public String getSection() {
		return section;
	}
	public void setSection(String section) {
		this.section = section;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	
	
}
